#!/usr/bin/env bash
# Consolidate the current reports/ into a dated markdown file committed to a
# dedicated results branch (default: "results"), e.g. runs/2026-07-01.md.
#
#   ./scripts/snapshot.sh              # uses today's date
#   ./scripts/snapshot.sh 2026-07-01   # explicit date/label
#   SNAPSHOT_BRANCH=results ./scripts/snapshot.sh
#
# Uses git plumbing so the working tree / current branch is never touched.
set -euo pipefail
cd "$(dirname "$0")/.."

DATE="${1:-$(date +%F)}"
BRANCH="${SNAPSHOT_BRANCH:-results}"
REL="runs/$DATE.md"

ls reports/*.results.json >/dev/null 2>&1 || { echo "no reports/ to snapshot — run a model first"; exit 1; }

TABLE="$(python3 scripts/summarize.py)"
PERMODEL="$(python3 - <<'PY'
import glob, json, os
for p in sorted(glob.glob("reports/*.results.json")):
    m = os.path.basename(p)[:-len(".results.json")]
    res = json.load(open(p))
    passed = sum(1 for r in res if r["status"] == "pass")
    fails = [r["project"] for r in res if r["status"] != "pass"]
    line = f"- **{m}** — {passed}/{len(res)}"
    print(line + (f" — failed: {', '.join(fails)}" if fails else " ✅"))
PY
)"

NTASKS="$(ls -d projects/*/ | wc -l | tr -d ' ')"

TMP="$(mktemp)"
{
  echo "# Benchmark run — $DATE"
  echo
  echo "$NTASKS tasks (tiers 1–3). Grading = hidden-test oracle."
  echo "Generated by \`scripts/snapshot.sh\` onto branch \`$BRANCH\`."
  echo
  echo "## Leaderboard"
  echo
  echo '```'
  echo "$TABLE"
  echo '```'
  echo
  echo "## Per model"
  echo
  echo "$PERMODEL"
} > "$TMP"

# --- commit to the results branch via plumbing (no checkout) ---
blob="$(git hash-object -w "$TMP")"
idx="$(mktemp -u)"; export GIT_INDEX_FILE="$idx"   # -u: name only, so git inits a fresh index
parent=""
if git rev-parse --verify -q "refs/heads/$BRANCH" >/dev/null; then
  parent="$(git rev-parse "refs/heads/$BRANCH")"
  git read-tree "$BRANCH"
fi
git update-index --add --cacheinfo "100644,$blob,$REL"
tree="$(git write-tree)"
if [ -n "$parent" ]; then
  commit="$(git commit-tree "$tree" -p "$parent" -m "run $DATE")"
else
  commit="$(git commit-tree "$tree" -m "run $DATE")"
fi
git update-ref "refs/heads/$BRANCH" "$commit"
unset GIT_INDEX_FILE; rm -f "$idx" "$TMP"

echo "snapshot: $REL -> branch '$BRANCH' ($(git rev-parse --short "$commit"))"
echo "view:  git show $BRANCH:$REL"
echo "list:  git ls-tree -r --name-only $BRANCH"
